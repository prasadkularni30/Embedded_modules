void Task1(void* argument)
{
	char data[]="Task T1 says Hello\r \n";            //message to send from UART
	while(1)
	{
		if(xSemaphoreTake(mutexPtr, 200/portTICK_PERIOD_MS) == pdPASS)			//acquiring semaphore
		{
			SEGGER_SYSVIEW_PrintfHost("Task1");

			HAL_UART_Transmit(&huart4, (uint8_t*)&data, strlen(data), 10);      //transmitting the message
			xSemaphoreGive(mutexPtr);
		}											//release semaphore
		vTaskDelay(500/portTICK_PERIOD_MS);
	}
}






